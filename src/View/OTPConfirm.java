        // TODO add your handling code here:
        //Button continue
        String enteredOTP = jTextField1.getText().trim();
        if(enteredOTP.length() != 6 || !enteredOTP.matches("\\d{6}")){
            JOptionPane.showMessageDialog(this,"PLease enter a valid OTP !");
            return;
        }
        if(enteredOTP.equals(correctOTP)){
            JOptionPane.showMessageDialog(this,"OTP Verified successfully !", "Success", JOptionPane.INFORMATION_MESSAGE);
            //Navigate to password reset form
            //Todo Code
            PasswordChange temp = new PasswordChange();
            temp.show();
            dispose();    
        }else{
            JOptionPane.showMessageDialog(this,"Invalid OTP. PLease try again.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
        }
